package simpleprogram;

public class Switch {

	public static void main (String [] args) {
		String vowel = "b";
		
		switch (vowel) {
		case "a":
			System.out.println("it's a vowel");
			break;
		case "e":
			System.out.println("it's a vowel");
			break;
		case "i":
			System.out.println("it's a vowel");
			break;
		case "o":
			System.out.println("it's a vowel");
			break;
		case "u":
			System.out.println("it's a vowel");
			break;
		default:
			System.out.println("it's not a vowel");
			break;
		}
	}
}
