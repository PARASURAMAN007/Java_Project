package simpleprogram;

public class Looping_st {

	public static void main (String [] args) {

		for (int i=0;i<50;i++) {
			System.out.println(i);
		}
		
		int j=5;
		while(j<10) {
			System.out.println(j);
			j++;
		}
		int k=5;
		do {
			k++;
		}
		while(k<20); {
			System.out.println(k);
		}
	}
}
